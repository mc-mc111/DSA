public class lec14 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 0;
        num2 = ++num1;
        System.out.println(num1);
        System.out.println(num2);
        num1 = 10;
        num2 = 0;
        num2 = num1++;
        System.out.println(num1);
        System.out.println(num2);        
    }
}
